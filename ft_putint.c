int	ft_putint(int n_int)
{
	char	c;
	int	i;

	if (n_int == -2147483648)
	{	
		ft_putstr("-2147483648");
		return ;
	}
	i = 0;
	if (n_int < 0)
	{
		n_int = -n_int;
		i = 1;
	}
	while (n_int > 1)
	{
		c = (n_int / 10) + '0';
		ft_putchar(c);
		i++;
	}
	return (i);	
}
